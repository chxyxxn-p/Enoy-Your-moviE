package com.eye.domain;

public class Place {
	private int place_no;
	private String first_sep;
	private String second_sep;
	
	public int getPlace_no() {
		return place_no;
	}
	public void setPlace_no(int place_no) {
		this.place_no = place_no;
	}
	public String getFirst_sep() {
		return first_sep;
	}
	public void setFirst_sep(String first_sep) {
		this.first_sep = first_sep;
	}
	public String getSecond_sep() {
		return second_sep;
	}
	public void setSecond_sep(String second_sep) {
		this.second_sep = second_sep;
	}
}
